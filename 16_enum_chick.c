/*
一共100只鸡，分为公鸡、母鸡、小鸡
公鸡每只5元，母鸡每只3元，小鸡每只1/3元
一共卖100元，问：公鸡、母鸡、小鸡各有几只？
*/

#include <stdio.h>
int main()
{
    int x,y,z;
    for(x=0;x<=20;x++){
        for(y=0;y<=33;y++){
            z=100-x-y;
            if((z%3==0)&&(5*x+3*y+z/3==100))
            printf("公鸡：%d，母鸡：%d，小鸡：%d\n",x,y,z);
        }
    }
    return 0;
}